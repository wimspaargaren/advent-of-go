func solve() (int, int) {
	inputs := getInputs()
	combinationStorePart1 := map[int][][]string{}
	combinationStorePart2 := map[int][][]string{}
	totalPart1 := 0
	totalPart2 := 0
	for _, input := range inputs {
		list := createList(input.list)
		combinationsPart1, ok := combinationStorePart1[len(list)-1]
		if !ok {
			combinationsPart1 = combis([]string{"+", "*"}, len(list)-1, []string{})
			combinationStorePart1[len(list)-1] = combinationsPart1
		}
		if execSymbols(input.res, combinationsPart1, list) {
			totalPart1 += input.res
		}
		combinationsPart2, ok := combinationStorePart2[len(list)-1]
		if !ok {
			combinationsPart2 = combis([]string{"+", "*", "||"}, len(list)-1, []string{})
			combinationStorePart2[len(list)-1] = combinationsPart2
		}
		if execSymbols(input.res, combinationsPart2, list) {
			totalPart2 += input.res
		}
	}
	return totalPart1, totalPart2
}

func execSymbols(expected int, symbols [][]string, list []int) bool {
	for _, instructions := range symbols {
		total := list[0]
		for i, instruction := range instructions {
			if instruction == "+" {
				total += list[i+1]
			} else if instruction == "*" {
				total *= list[i+1]
			} else if instruction == "||" {
				new := fmt.Sprintf("%d%d", total, list[i+1])
				i, err := strconv.Atoi(new)
				if err != nil {
					panic(err)
				}
				total = i
			}
		}
		if total == expected {
			return true
		}
	}
	return false
}

func createList(input string) []int {
	list := strings.Split(input, " ")
	intList := []int{}
	for _, v := range list {
		i, err := strconv.Atoi(v)
		if err != nil {
			panic(err)
		}
		intList = append(intList, i)
	}
	return intList
}

func combis(symbols []string, length int, current []string) [][]string {
	result := [][]string{}
	if length == 0 {
		return append(result, current)
	}
	for _, symbol := range symbols {
		temp := make([]string, len(current))
		copy(temp, current)
		result = append(result, combis(symbols, length-1, append(temp, symbol))...)
	}

	return result
}

